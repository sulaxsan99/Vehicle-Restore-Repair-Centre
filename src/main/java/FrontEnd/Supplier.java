    public void loadComboBoxx() throws SQLException {
        stm = con.createStatement();
        rs = stm.executeQuery("select SupID from suppliert");
        SupIDcomboBox.removeAllItems();
        while (rs.next()) {
            String name = rs.getString("SupID");
            SupIDcomboBox.addItem(name);
        }
    }
